DROP TABLE CLIENTES IF EXISTS;

CREATE TABLE CLIENTES (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1000, INCREMENT BY 1) PRIMARY KEY,
  APELLIDOS VARCHAR(50),
  PUNTOS BIGINT
);

DROP TABLE USUARIOS IF EXISTS;

CREATE TABLE USUARIOS (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1000, INCREMENT BY 1) PRIMARY KEY,
  NOMBRE VARCHAR(50),
  USR_LOGIN VARCHAR(25),
  PWD VARCHAR(25),
  ROL VARCHAR(25)
);

DROP TABLE OFERTAS IF EXISTS;

CREATE TABLE OFERTAS (
  ID VARCHAR(25) PRIMARY KEY,
  NOMBRE VARCHAR(25),
  DESC VARCHAR(150)
);

DROP TABLE ITEMS IF EXISTS;

CREATE TABLE ITEMS (
  ID VARCHAR(25) PRIMARY KEY,
  NOMBRE VARCHAR(25),
  DESC VARCHAR(150)
);



